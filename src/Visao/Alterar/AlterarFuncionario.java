    private void bTN_OkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTN_OkActionPerformed
    String codigo = jTF_cod.getText();
    Connection con = Conexao.AbrirConexao();
    FuncionarioDAO sql = new FuncionarioDAO(con);
    int cod = Integer.parseInt(codigo);
    
        if (codigo.equals("")) {
            JOptionPane.showMessageDialog(null, "Digite um c칩digo para atualizar",
                        "Video Locadora", JOptionPane.WARNING_MESSAGE);
        }
            
            jTF_Codigo.setText("");
            jTF_Nome.setText("");   
            jTF_Login.setText("");   
            jTF_SenhaAt.setText("");   
            
    InserirDados(cod);
    jTF_cod.setText("");
    }//GEN-LAST:event_bTN_OkActionPerformed

    private void bTN_AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTN_AlterarActionPerformed
    String codigo = jTF_Codigo.getText();
    String nome = jTF_Nome.getText();
    String login = jTF_Login.getText();
    String senha = jPF_SenhaAlt.getText();
    
    if (nome.equals("") || login.equals("") || senha.equals("")) {
        JOptionPane.showMessageDialog(null, "Nenhum campo pode estar vazio!",
                    "Video Locadora", JOptionPane.WARNING_MESSAGE);
    } else {
        Connection con = Conexao.AbrirConexao();
        FuncionarioDAO sql = new FuncionarioDAO(con);
        int cod = Integer.parseInt(codigo);
        Funcionario a = new Funcionario();
        
        a.setCod(cod);
        a.setNome(nome);
        a.setLogin(login);
        a.setSenha(senha);
        
        sql.Alterar_Funcionario(a);
        Conexao.FecharConexao((com.mysql.jdbc.Connection) con);

        jTF_Codigo.setText("");
        jTF_Nome.setText("");
        jTF_Login.setText("");
        jTF_SenhaAt.setText("");
        jPF_SenhaAlt.setText("");
        
        JOptionPane.showMessageDialog(null, "Update realizado com sucesso!",
                    "Video Locadora", JOptionPane.INFORMATION_MESSAGE);   
    }
    }//GEN-LAST:event_bTN_AlterarActionPerformed

